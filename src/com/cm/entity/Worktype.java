package com.cm.entity;

public class Worktype {

		private int id;
		private String name;
		private int specia;//是否特殊人员1：是2：否
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getSpecia() {
			return specia;
		}
		public void setSpecia(int specia) {
			this.specia = specia;
		}
		
	
}
