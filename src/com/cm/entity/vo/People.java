package com.cm.entity.vo;

public class People {
	int card;
	int battary;
	int emerge;
	String reader;
	public int getCard() {
		return card;
	}
	public void setCard(int card) {
		this.card = card;
	}
	public int getBattary() {
		return battary;
	}
	public void setBattary(int battary) {
		this.battary = battary;
	}
	public int getEmerge() {
		return emerge;
	}
	public void setEmerge(int emerge) {
		this.emerge = emerge;
	}
	public String getReader() {
		return reader;
	}
	public void setReader(String reader) {
		this.reader = reader;
	}
}
